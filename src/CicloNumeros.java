
public class CicloNumeros {
    public static void main(String[] args) {
	     // Ciclo externo para el primer número (1, 2, 3)
	       for (int i = 1; i <= 3; i++) {
	           // Ciclo interno para el segundo número (1, 2, 3)
	           for (int j = 1; j <= 3; j++) {
	               System.out.println(i + " " + j);
	           }
	       }
	   }
}


